public class TipeData {
    public static void main(String[] args) {
        // Tpe data di java
        // Integer, Byte, Short, Long, Double, Float, Char, Boolean


        // Integer  (Satuan)
        System.out.println("Integer");
        int i = 20;
        System.out.println("Nilai integer 1 = " + (i+ 1));
        System.out.println("Nilai max = " + Integer.MAX_VALUE);
        System.out.println("Nilai min = " + Integer.MIN_VALUE);
        System.out.println("Besar integer = " + Integer.BYTES + " Byte");
        System.out.println("Besar integer = " + Integer.SIZE + " bit");

        System.out.println("");
        System.out.println("");

         // byte  (Satuan)
         System.out.println("Byte");
         byte b = 10;
         System.out.println("Nilai byte 1 = " + b );
         System.out.println("Nilai max = " + Byte.MAX_VALUE);
         System.out.println("Nilai min = " + Byte.MIN_VALUE);
         System.out.println("Besar byte = " + Byte.BYTES + " Byte");
         System.out.println("Besar byte = " + Byte.SIZE + " bit");

         System.out.println("");
         System.out.println("");
 
          // byte  (Satuan)
          System.out.println("Short");
          short c = 10;
          System.out.println("Nilai short 1 = " + c );
          System.out.println("Nilai max = " + Short.MAX_VALUE);
          System.out.println("Nilai min = " + Short.MIN_VALUE);
          System.out.println("Besar short = " + Short.BYTES + " Byte");
          System.out.println("Besar short = " + Short.SIZE + " bit");

          System.out.println("");
          System.out.println("");

         // byte  (Satuan)
         System.out.println("Long");
         long d = 10L;
         System.out.println("Nilai long 1 = " + d );
         System.out.println("Nilai max = " + Long.MAX_VALUE);
         System.out.println("Nilai min = " + Long.MIN_VALUE);
         System.out.println("Besar long = " + Long.BYTES + " Byte");
         System.out.println("Besar long = " + Long.SIZE + " bit");

         System.out.println("");
         System.out.println("");

         // byte  (koma, bilangan real)
         System.out.println("Double");
         double e = -10.5d;
         System.out.println("Nilai double 1 = " + e );
         System.out.println("Nilai max = " + Double.MAX_VALUE);
         System.out.println("Nilai min = " + Double.MIN_VALUE);
         System.out.println("Besar double = " + Double.BYTES + " Byte");
         System.out.println("Besar double = " + Double.SIZE + " bit");


         System.out.println("");
         System.out.println("");

         // byte  (Satuan)
         System.out.println("Float");
         float f = -8.5f;
         System.out.println("Nilai float 1 = " + f );
         System.out.println("Nilai max = " + Float.MAX_VALUE);
         System.out.println("Nilai min = " + Float.MIN_VALUE);
         System.out.println("Besar float = " + Float.BYTES + " Byte");
         System.out.println("Besar float = " + Float.SIZE + " bit");


         System.out.println("");
         System.out.println("");

         // Char  (karakter)  berdasarkan ASCII
         System.out.println("Char");
         char g = 'G';
         System.out.println("Nilai char 1 = " + g );
         System.out.println("Nilai max = " + Character.MAX_VALUE);
         System.out.println("Nilai min = " + Character.MIN_VALUE);
         System.out.println("Besar char = " + Character.BYTES + " Byte");
         System.out.println("Besar char = " + Character.SIZE + " bit");


         System.out.println("");
         System.out.println("");

         // Boolean  (true & False)  
         System.out.println("Boolean");
         boolean h= false;
         System.out.println("Nilai boolean 1 = " + h );
         System.out.println("Nilai max = " + Boolean.TRUE);
         System.out.println("Nilai min = " + Boolean.FALSE);
    }
}
